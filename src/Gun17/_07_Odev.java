package Gun17;

public class _07_Odev {
    public static void main(String[] args) {
        //int Array oluşturun ve elemanları : 12,2,5,15,8
        //En büyük değeri yazdırınız.
        int[] dizi = {12, 2, 5, 15, 8};
        int max = 0;
        for (int i = 0; i < dizi.length; i++) {
            if (i > max) {
                max = i;
            }
        }
        System.out.println(max);



    }





    }





