package Gun09;

public class _01_JavaAritmeticOperators {
    public static void main(String[] args) {
        int a=4;
        int b=5;

        System.out.println("toplama işelmi");
        System.out.println("a+b = " + (a+b) );

        System.out.println("çıkartma işelmi");
        System.out.println("a-b = " + (a-b) );

        System.out.println("çarpma işelmi");
        System.out.println("a*b = " + (a*b) );

        System.out.println("bölme işelmi");
        System.out.println("a/b = " + (a/b) );

        System.out.println("Modül işelmi");
        System.out.println("a%b = " + (a%b) );
    }
}
